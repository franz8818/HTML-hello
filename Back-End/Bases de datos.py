#BASES DE DATOS
#Conjunto de datos pertenecientes a un mismo contexto y almacenados sistemáticamente para su posterior uso.
#Ejemplo -> Biblioteca, jeroglíficos, cuadernos, blog de notas, excel.

#SQL -> Structured Query Language
#Relacional -> Basada en tablas
#Mantienen una estructura definida
#Aplicaciones para datos consistentes -> Ejemplo -> Datos bancarios
#Herramientas Relacionales -> MySQL, Oracle, Microsoft Access

#NoSQL -> Structured Query Language
#No Relacional -> Basada en documentos
#Estructura dinámica
#Aplicaciones que requieren grandes cantidades de datos a consultar -> Ejemplo -> Datos de las Redes Sociales
#Herramientas No Relacionales -> MongoDB, AzureDB, DynamoDB



